export function isEditableTextFile(relativePath: string, isDir = false): boolean {
  if (isDir) return false;
  const name = relativePath.split(/[/\\]/).pop() || relativePath;
  const lowerName = name.toLowerCase();

  if (EDITABLE_BASENAMES.has(lowerName)) return true;

  const dot = lowerName.lastIndexOf(".");
  if (dot <= 0 || dot === lowerName.length - 1) {
    // No extension / leading-dot files: allow common text-ish names only via basenames above.
    return lowerName.startsWith(".") && EDITABLE_BASENAMES.has(lowerName);
  }

  const ext = lowerName.slice(dot + 1);
  return EDITABLE_EXTENSIONS.has(ext);
}
